// 개인정보(PII) 마스킹 유틸 (OPEN-6)
// 채팅 모니터링 등에서 전화번호/이메일 등 개인정보 노출을 방지한다.
// 실 열람은 신고 방/메시지 범위 제한 + 열람 감사 후 원문 노출(차기).

/** 전화번호/이메일 등 PII 를 마스킹한 문자열 반환 */
export function maskPii(text: string): string {
  return text
    .replace(/01[016789][-\s]?\d{3,4}[-\s]?\d{4}/g, "010-****-****")
    .replace(/[\w.+-]+@[\w-]+\.[\w.-]+/g, "***@***");
}
